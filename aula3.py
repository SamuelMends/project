"""
Python = Linguagem de programação 
Tipo de tipagem = Dinâmica/Forte
str -> string -> texto
Strings são textos que estão dentro de aspas.
"""

#Aspas simples
print('Luiz Otavio')

#Aspas duplas
print("Luiz Otavio")

#Escape (Adiciona aspas a frase)
print("Luiz \"Otavio\"")

#r Adiciona as barras
print(r"Luiz \"Otavio\"")